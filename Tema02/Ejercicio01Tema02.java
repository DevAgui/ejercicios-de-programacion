/**
 * Uso de variables que contienen números decimales
 *
 * @author Diego Aguilera Martín
 */
public class Ejercicio01Tema02 {
  public static void main(String[] args) {
  
    int x = 144;
    
    int y = 999;
    
    int suma = x + y;
    
    int resta = y - x;
    
    int division = y / x;
    
    int multiplicacion = x * y;
    
    System.out.println("El valor de x " + x);
    
    System.out.println("El valor de y " + y);
    
    System.out.println("El valor de la suma de x + y es " + suma);
    
    System.out.println("El valor de la resta de x - y es " + resta);
    
    System.out.println("El valor de la división de y / x es " + division);
    
    System.out.println("El valor de la multiplicación de x por y es " + multiplicacion);
  }
}
